package com.tajiang.leifeng.model;


public class ThirdAccount  {
	
	private String loginname;
	
	private String password;
	
	private String userId;
	
	private String thirdAccountName;
	
	private Integer thirdAccountType;
	
	private Integer sex;
	
	private Byte archive;
	
	private String avatar;
	
	public String getLoginname() {
		return loginname;
	}

	public void setLoginname(String loginname) {
		this.loginname = loginname;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getThirdAccountName() {
		return thirdAccountName;
	}

	public void setThirdAccountName(String thirdAccountName) {
		this.thirdAccountName = thirdAccountName;
	}

	public Integer getThirdAccountType() {
		return thirdAccountType;
	}

	public void setThirdAccountType(Integer thirdAccountType) {
		this.thirdAccountType = thirdAccountType;
	}

	public Integer getSex() {
		return sex;
	}

	public void setSex(Integer sex) {
		this.sex = sex;
	}

	public Byte getArchive() {
		return archive;
	}

	public void setArchive(Byte archive) {
		this.archive = archive;
	}

	public String getAvatar() {
		return avatar;
	}

	public void setAvatar(String avatar) {
		this.avatar = avatar;
	}

}